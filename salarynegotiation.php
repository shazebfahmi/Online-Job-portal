                <h2>SALARY NEGOTIATIONS</h2>
                <p>These are Our few steps for employers to mind during salary negotiations</p>
                <p><i class="fa fa-hand-o-right"></i><strong>Be sure your performance merits a raise before you ask for one</strong></p>
		<prev>If you are going to try negotiating for a bump in your current salary, be sure you can show that it is warranted. One of the biggest mistakes that can ruin a salary negotiation is not having proof that you are indispensable to your organization.  If the employees had shown they are really worth their salt, by showing up to work on time and working as hard as they could be given a raise</prev>
                <p><i class="fa fa-hand-o-right"></i><strong> Treat it as a collaboration, not a fight.</strong></p>
                 <prev>Never engage in negotiation as an ultimatum  an either/or  but rather as a collaborative process and a unique opportunity to create a compensation package that makes sense for both you and for them.Establish priorities as to what is most important to you and what items you are willing to trade off. Then, make your case and say that you are looking forward to working together</prev>
                <p><i class="fa fa-hand-o-right"></i><strong>Wait as long as you can before discussing salary.</strong></p>
		<prev>This one is especially true if you are trying to score a great salary at your first job, but it is applicable to all job seekers. In addition to avoiding naming a number that you are looking for in terms of salary,you also want to defer the salary conversation as long as possible, because the longer you can defer that discussion, the more time you have to impress them in your interviews and convince them that you should be paid at the higher end of the range they have budgeted for the role.</prev>
		<p><i class="fa fa-hand-o-right"></i><strong>Don not use your personal circumstances as a negotiation tool.</strong></p>
		<prev>While it might feel logical to explain your personal financial situation as your reasoning for needing to earn more money, experts say this can also ruin your salary negotiation. an employee can create a more compelling argument for a raise by providing evidence of his or her hard work rather than emphasizing the financial strugleness.</prev>
		<p><i class="fa fa-hand-o-right"></i><strong>Never apologize.</strong></p>
		<prev> 'sorry' is another thing you should never say in a salary negotiation.Negotiating is uncomfortable, and our natural tendency is to try to smooth the edges on a difficult conversation. Saying sorry could signal to the recruiter or hiring manager that you might be willing to back down, and that could be expensive. Don not apologize for negotiating.</prev>
