                <p><i class="fa fa-hand-o-right"></i><strong> Take a personal interest</strong></p>
		   <prev>-Make time to meet regularly with team members on a one-to-one basis to learn about their aspirations, expectations and frustrations. This outreach can help make your staff feel valued  and lead to greater productivity and loyalty.

Also, help your employees to plot a career path within the company, so they can better visualize their future at your firm. Identify specific milestones for achievement, and the supporting resources employees will likely need to tap along their journey. Clear and consistent communication about career advancement opportunities can help workers feel more engaged and empowered.</prev>
		<p><i class="fa fa-hand-o-right"></i><strong> Focus on learning</strong></p>
		<prev>-Continued job training and education are important to an employee's professional development. Millennial workers, in particular, tend to covet these learning opportunities. Ensure that courses and workshops  paid for by the company  are part of their career plan.

In addition to nurturing individual needs and specific skills, help employees keep up with what is happening in the wider industry. One cost-effective method is to host monthly lunchtime sessions with guest speakers on relevant topics, such as cloud technology or tax legislation changes. Also, reward top talent by sending them to industry conferences where they can learn and network.</prev>
		<p><i class="fa fa-hand-o-right"></i><strong> Rotate employee roles</strong></p>
		<prev>-The human brain thrives on variety, and job rotation is a smart way to shake up your workers daily routine. Encourage staff to work in different but related departments or positions. (A job rotation program can help to facilitate this.) These opportunities will help them to gain new skills, more appreciation of their colleagues duties and a better understanding of the business. Meanwhile, the company, and you, will benefit from having a more motivated, informed and well-rounded accounting and finance team.</prev>
		<p><i class="fa fa-hand-o-right"></i><strong> Support work-life balance</strong></p>
		<prev>-Hard work is a prerequisite for career advancement, but that does not necessarily mean regularly putting in 10-12 hours a day. Encourage your employees to work smart, maximize their efficiency, and leave time and energy for interests outside of work.

Take a proactive approach to your team's well-being rather than waiting for problems like stress and burnout to bubble up. Embracing flexible policies that are designed to enhance work-life balance can make a huge difference to staff morale. It helps employees find the time to do their work, enjoy their life outside of the office, and take advantage of available professional development opportunities.</prev>
		<p><i class="fa fa-hand-o-right"></i><strong> Encourage mentoring</strong></p>
		<prev>- Establishing a formal mentoring program might be one of the smartest moves a company can make  for itself as well as for its workers personal and career growth.

Though often seen as a transfer of knowledge from veteran employees to the less experienced, mentoring works both ways. While senior staff members can offer hard-earned insights and professional guidance to their junior colleagues, they also can benefit from the fresh perspectives and technological know-how of younger generations in the workforce.</prev>
