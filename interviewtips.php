		<h2>INTERVIEW TIPS</h2>
                <p>These are Our few steps for employers to can follow to ace the interview successfully.</p>
                <p><i class="fa fa-hand-o-right"></i><strong>Have IT Soft Skills</strong></p>
		<prev>Soft skills are valued in today's workforce, regardless of your job. Your future boss wants to make sure she can trust you to interact well with peers, suppliers and, most importantly, clients. Employers are becoming more willing to invest in a well-spoken junior resource who can be trained rather than a guru who does not communicate well.
                          To wit: Be friendly, stay natural and show how awesome you are. Many candidates are understandably nervous and assume more rigid personas during interviews. Don't. You have a personality -- use it! Always remember, if hiring managers are taking the time to meet with you, they want to like you. It is in their interest. Let them.</prev>
                <p><i class="fa fa-hand-o-right"></i><strong>Be Honest About Your Knowledge</strong></p>
                 <prev>A necessary evil of applying for an IT job is the technical interview. This is an often a harrowing affair in which there is often a very defined answer to a question. You either know the answer or you don't, so be honest if you don't. Nothing makes an interviewer more uncomfortable than a candidate taking wild guesses when answering interview questions. Doing so will make you sound and feel insecure, and will surely impact the tone of the meeting.</prev>
                <p><i class="fa fa-hand-o-right"></i><strong>Steer the Conversation With Eye-to-Eye Contact</strong></p>
		<prev>This is a universal interview tactic: Try to keep the conversation focused on your strengths. In fact, go one step further. If you are applying for a Unix system administrator position and you have, say, experience managing Active Directory, it does not hurt to discuss it.An IT-oriented organization is always looking for multiskilled talent who can fill in knowledge gapsKeep in mind that companies hate turning away a multitalented candidate, unless of course the candidate has an irritating personality.
                  </prev>
		<p><i class="fa fa-hand-o-right"></i><strong>Have a Positive Approach</strong></p>
		<prev>Interviewers love enthusiastic candidates. If you come across as confident and positive, your interviewer will be more at ease and more likely to want to engage you. In addition to your technical aptitude and personality, an interviewer wants to make sure you will be happy if you are offered the position. Feel free to ask about training programs and professional IT certification as a means of showing a passion for learning and advancement.
</prev>
		<p><i class="fa fa-hand-o-right"></i><strong>Practice, Practice, Practice</strong></p>
		<prev>You, and everyone else interviewing for the job, already know many of the questions you will be asked. The difference lies in preparation. Preparing unique and position-specific responses will give you the competitive edge over everyone else. You don't need to memorize answers, but instead know certain points of reference about yourself that you can apply to different questions.

Make sure to "mock interview" yourself. Video your responses until you are able to speak comfortably and flexibly--as opposed to rotely regurgitating answers--about your prepared topics. Videoing yourself may feel awkward when you do it, but it will pay off during your interview.</prev>
